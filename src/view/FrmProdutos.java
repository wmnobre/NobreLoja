    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {                                          
        
        String nome = txtNome.getText();
        
        if( nome.isEmpty() || cmbProdutos.getSelectedIndex() == 0 ){
            JOptionPane.showMessageDialog(null, 
                    "Nome e Produtos são obrigatórios!" );
        }else{
            boolean nova = false;
            if( produto == null ){
                nova = true;
                produto = new Produtos();
            }
            
            produto.setNome( nome );
            produto.setProdutos( (Produtos) cmbProdutos.getSelectedItem() );
            if( nova ){
                ProdutoDAO.inserir(produto);
                produto = null;
            }else{
                ProdutoDAO.editar( produto );
                
                telaListProdutos.carregarTabela();
                this.dispose();
            }
            // limpar o formulário
            txtNome.setText("");
            cmbProdutos.setSelectedIndex( 0 );
        }
        
    }                                         

