    private void carregarFormulario(){
        lblCodigoValor.setText( String.valueOf( produto.getCodigo( )) );
        txtNome.setText( produto.getNome() );
        
        List<Produtos> estados = ProdutoDAO.getProd();
        for( int i = 0; i < estados.size(); i++){
            if( estados.get(i).getCodigo() == produto.getProdutos().getCodigo() ){
                int posicao = i + 1;
                cmbProdutos.setSelectedIndex(posicao);
                break;
            }
        }
        
    }
    
    private void carregarProdutos(){
        List<Produtos> lista = ProdutoDAO.getProdutos();
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        
        Produtos fake = new Produtos("Selecione...");
        model.addElement(fake);
        
        for (Produtos estado : lista) {
            model.addElement( estado );
        }
        cmbProdutos.setModel( model );
    }

