    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
        int linha = tableClientes.getSelectedRow();
        if (linha < 0){
            JOptionPane.showMessageDialog(this, "Um cliente deve ser selicionado!");
        }else{
            
            int codigo = (int)tableClientes.getValueAt(linha, 0);
            
            FrmCliente tela = new FrmCliente(codigo, this);
            this.jdpTelaInicial.add(tela);
            tela.setVisible(true);
            
            
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        int linha = tableClientes.getSelectedRow();
        if (linha < 0){
            JOptionPane.showMessageDialog(this, "Um cliente deve ser selicionado!");
        }else{
            
            int codigo = (int)tableClientes.getValueAt(linha, 0);
            Cliente cliente = ClienteDAO.getClienteBycodigo(codigo);
            
            int resposta = JOptionPane.showConfirmDialog(this, " Confirma a exclusão do cliente " + cliente.getNome() + "?","Excluir Cliente",
                     JOptionPane.YES_NO_OPTION);
            
            if (resposta == JOptionPane.YES_NO_OPTION){                
                        
            ClienteDAO.excluir(cliente);
            carregarTabela();
        }
        
        
        }
    }//GEN-LAST:event_btnExcluirActionPerformed


