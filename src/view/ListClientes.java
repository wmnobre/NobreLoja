    public void carregarTabela(){
        List<Cliente> lista = ClienteDAO.getClientes();
        String[] colunas = {"Código", "Nome", "Telefone", "CPF", "Data de Nascimento", "Sexo", "Casado?", "Filhos? "
                + "Salário", "Cidade", "Estado"};
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(colunas);
        for(Cliente cliente: lista){
            Calendar nasc = cliente.getNascimento();
            String data = ""+
                    nasc.get(Calendar.DAY_OF_MONTH) +
             "/" + (nasc.get(Calendar.MONTH)+1) +
             "/" +  nasc.get(Calendar.YEAR);
            
            String casado = "Não";
            if (cliente.isCasado())
                casado = "Sim";
            String filhos = "Não";
            if (cliente.isTemFilhos())
                filhos = "Sim";
            String sexo = "";
            if (cliente.getSexo().equals("f"))
            sexo = "Feminino";
            if (cliente.getSexo().equals("m"))
                sexo = "Masculino";
            
                    
                    
                    
            
            
            Object[] dados = {
                
                cliente.getCodigo(),
                cliente.getNome(),
                cliente.getTelefone(),
                cliente.getCpf(), data, sexo, casado, filhos, cliente.getSalario(),
                cliente.getCidade().getNome(),
                cliente.getCidade().getEstado().getNome(),
                
                
            };
            
            model.addRow(dados);             
           
        }
        
        tableClientes.setModel(model);
    }

